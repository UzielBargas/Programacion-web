var DOLAR = 1000;  
var REAL = 200;    
var EURO = 1100;   

function pesosADolares(pesos) {
    return pesos / DOLAR;
}

function dolaresAPesos(dolares) {
    return dolares * DOLAR;
}

function pesosAReales(pesos) {
    return pesos / REAL;
}

function realesAPesos(reales) {
    return reales * REAL;
}

function pesosAEuros(pesos) {
    return pesos / EURO;
}

function eurosAPesos(euros) {
    return euros * EURO;
}
